def get_template_preview(
    task: str = "image_classification",
    model: str = "resnet50",
) -> dict[str, Any]:
    """获取模板预览（只显示脚本的关键部分）

    Returns:
        {
            "header": "脚本头部信息",
            "key_parts": ["关键代码片段"],
            "total_lines": 预计行数
        }
    """
    preview_script = generate_training_script(
        task=task,
        model=model,
        dataset="cifar10",
        hardware="Ascend",
        num_epochs=90,
        batch_size=32,
        base_lr=0.001,
        optimizer="adam",
        lr_scheduler="cosine",
        use_amp=True,
    )

    lines = preview_script["script"].split("\n")

    return {
        "header": "\n".join(lines[:20]),
        "key_parts": [
            "\n".join(lines[20:50]),   # 数据加载
            "\n".join(lines[50:80]),   # 模型定义
            "\n".join(lines[80:120]),   # 训练配置
            "\n".join(lines[-30:]),     # 主函数
        ],
        "total_lines": len(lines),
        "filename": preview_script["filename"],
    }
